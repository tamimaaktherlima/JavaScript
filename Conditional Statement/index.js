var num = 5;

// Check Even Odd
if(num % 2 == 0){
    console.log("Even\n");
}
else{
    console.log("Odd\n");
}

// Check Positive or Negative
if(num > 0){
    console.log("Positive\n");
}
else if(num < 0){
    console.log("Negative\n");
}
else{
    console.log("Equal to Zero\n");
}